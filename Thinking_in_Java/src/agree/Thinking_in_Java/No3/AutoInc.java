package agree.Thinking_in_Java.No3;

public class AutoInc {
	public static void main(String[] args) {
		int i = 1;
		System.out.println(" i : "+i);
		System.out.println(" ++i : "+ ++i);
		System.out.println(" i++ : "+i++);
		
		System.out.println(" i : "+i);
		System.out.println(" -- i : "+ -- i);
		System.out.println(" i-- : "+i-- );
		System.out.println(" i : "+i);
	}
}
